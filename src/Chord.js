	/**
	 * constructor
	 *
	 * @param name is the name of the chord
	 */
	constructor: function (name, nocheck) {
		this.inherited(arguments);
		this._name=name;
		this._definitionList=[];
		if(!nocheck) {
			var def = udo.Chords.defaultDefs[name];
			if (def) this._definitionList=def;
		}
	},
	
	/**
	 * draw
	 *
	 * draws the chord pattern for a given chord in a given canvas
	 */
	draw: function (pref) {
		// is this a isUserChord definition?
		var isUserChord = (this._definitionList.length>0 && this._definitionList[this._voicing].indexOf("udefine")!=-1); 
		// set the fill and stroke color of the graphics context according to the canvas' style
		var canvasD = document.getElementById(pref+'_chord'+this._name);	// standard canvas
		var canvasU= document.getElementById(pref+'_chordU');			// user chord canvas
		var canvas = isUserChord ? canvasU : canvasD;
		var ctx = canvasD.getContext("2d");
		var fontFamily;
		if (canvas.currentStyle) { // IE, Opera
			ctx.fillStyle = canvas.currentStyle["color"];
			ctx.strokeStyle = canvas.currentStyle["color"];
			fontFamily=canvas.currentStyle["fontFamily"];
			ctx.font=canvas.currentStyle["fontSize"]+' '+fontFamily+' '+canvas.currentStyle["fontStyle"];
		} else if (window.getComputedStyle) { // Firefox, Chrome
			var computedStyle = window.getComputedStyle(canvas, null);
			ctx.fillStyle = computedStyle.color;
			ctx.strokeStyle = computedStyle.color;
			fontFamily=computedStyle.fontFamily;
			ctx.font=computedStyle.fontSize+' '+fontFamily+' '+computedStyle.fontStyle ;
		} else {
			alert('can\'t get the canvas style for your browser!');
		}
		// normaize canvas size to 100%
		var canvasHeight=100;	
		var canvasWidth=100;
		// set height of chord title
		var titleHeight = 25;
		// set position and dimentions of string labels
		var labelHeight = 10;
		var labelSep = 5;
		var labelStart = titleHeight + labelSep;
		var labelEnd = labelStart+labelHeight;
		// set position and dimention of frets
		var fretWidth = 1;
		var fretSep = fretWidth+1;
		var fretStart = labelEnd+fretSep;
		var fretEnd = canvasHeight - fretSep;
		var fretDist = (fretEnd - fretStart )  / 5;
		//set position and dimenstions of strings
		var stringWidth = 1;
		var stringSep=5;
		var stringStart=stringSep;
		var stringEnd=stringStart+fretEnd-fretStart;
		var stringDist = (stringEnd - stringStart) / 5;
		// set position and dimentions of base fret label
		var baseXPos = (stringEnd + canvasWidth) / 2;
		var baseYPos = fretStart + fretDist;
		var baseHeight = 2*fretDist;
		//set position and dimention of variations label
		var variationsXPos = baseXPos;
		var variationsYPos = fretStart + 3*fretDist;
		var moreHeight = fretDist;
		// set radiius of finger marks
		var fingerRadius = fretDist / 2 - stringWidth;
		if (stringDist / 2 < fingerRadius) fingerRadius = stringDist / 2;
		
		// clear the canvas
		ctx.clearRect(0, 0, canvasWidth, canvasHeight);
		// draw title
		ctx.font="bold "+(titleHeight*.9)+"px arial";
		ctx.textAlign="center";
		ctx.textBaseline="bottom";
		ctx.fillText(this._name,canvasWidth/2,titleHeight);

		// unknown chord, nothing more to draw
		if (this._definitionList.length==0) return;
			
		// get base fret number and finger positions
		var def = this._definitionList[this._voicing].replace(/{|}/g, '').split(' ');
		var defx = def.splice(5);
		var base= def[3];

		
		// draw base fret if greater than 1
		if (base>1) {
			ctx.font="bold "+baseHeight+"px "+fontFamily;
			ctx.textAlign="center";
			ctx.textBaseline="middle";
			ctx.fillText(base,baseXPos,baseYPos);
		}

		// draw variation numbers if greater than 1
		if (this._definitionList.length>1) {
			ctx.font="bold "+moreHeight+"px "+fontFamily;
			ctx.textAlign="center";
			ctx.textBaseline="middle";
			ctx.fillText((this._voicing+1)+"/"+this._definitionList.length,variationsXPos,variationsYPos);
		}
			
		// draw the frets
		for ( var f = 0, i = fretStart; f < 6; f++, i += fretDist) {
			if (base == 1 & f == 0) ctx.lineWidth = 2 * fretWidth;
			else ctx.lineWidth = fretWidth;
			drawLine(stringStart, i, stringEnd, i);
		}

		// draw the strings 
		ctx.lineWidth = stringWidth;
		for ( var s = 0, i = stringStart; s < 6; s++, i += stringDist) {
			drawLine(i, fretStart, i, fretEnd);
		}

		// draw finger marks and string labels */
		ctx.lineWidth = 2 * fretWidth;
		for ( var d in defx) {
			var dx = stringSep + d * stringDist;
			if (defx[d] == "X") {
				drawLine(dx - stringSep / 2, labelStart, dx + stringSep / 2, labelEnd);
				drawLine(dx - stringSep / 2, labelEnd, dx + stringSep / 2, labelStart);
			} else if (defx[d] == 0) {
				drawCircle(dx, labelStart + labelHeight / 2, labelHeight / 2 - fretWidth);
			} else {
				var dy = defx[d] * fretDist + fretStart;
				drawCircle(dx, dy - fingerRadius, fingerRadius, "Y");
			}
		}

		/**
		 * drawLine
		 * 
		 * draws a straight line
		 * 
		 * @param fromX is the x value of start point
		 * @param fromY is the y value of start point
		 * @param toX is the x value of end point
		 * @param toY is the y value of end point
		 */
		function drawLine(fromX, fromY, toX, toY) {
			ctx.beginPath();
			ctx.moveTo(fromX, fromY);
			ctx.lineTo(toX, toY);
			ctx.stroke();
		}

		/**
		 * drawsCircle
		 * 
		 * draws a circle
		 * 
		 * @param x is the x value of center
		 * @param y is the y value of center
		 * @param radius is the radius of the circle
		 * @param fill is "Y" for a filled circle
		 */
		function drawCircle(x, y, radius, fill) {
			ctx.beginPath();
			ctx.arc(x, y, radius, 0, 7);
			if (fill == 'Y') ctx.fill();
			else ctx.stroke();
		}
	},
	
	/**
	 * next
	 *
	 * switches to the next voicing of the chord
	 */
	next: function () {
		this._voicing++;
		if (this._voicing==this._definitionList.length) this._voicing=0;
	},
	
	/**
	 * addDefinition
	 *
	 * adds a new voicing to the end of the list if it is not yet in the list
	 *
	 * @param definition is the chordpo definition of the voicing
	 */
	addDefinition: function (definition) {
		if (!this.has(definition)) this._definitionList.push(definition);
	},
	
	/**
	 * insert
	 *
	 * adds a new voicing to the start of the list
	 *
	 * @param definition is the chordpo definition of the voicing
	 */
	insertDefinition:  function (definition) {
		if (!this.has(definition)) this._definitionList.splice(0,0,definition);
	},
	
	/**
	 * has
	 *
	 * checks whether the given voicing is in already the list
	 *
	 * @param definition is the chordpo definition of the voicing
	 * @return true if the voicing is already in the list
	 */
	has: function (definition) {
		for(var d in this._definitionList) {
			if(this._definitionList[d]==definition) return true;
		}
		return false;
	}
